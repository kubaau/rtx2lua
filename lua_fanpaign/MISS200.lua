function triggerEndEvent(e, trigger) -- events triggered from other events
    if not activeEvents[e] then return end

    if timesTriggered[e] then timesTriggered[e] = timesTriggered[e] + 1 end

    if false then -- dummy if
    elseif e == 0 then
        showMissionText(0)
    elseif e == 1 then
        showMissionText(2)
    elseif e == 4 then
        enableBuilding(BLD_SHIPYARD)
    elseif e == 5 then
        enableBuilding(BLD_MINT)
    elseif e == 6 then
        enableBuilding(BLD_CATAPULT)
    elseif e == 50 then
        rttr:GetWorld():AddStaticObject(234, 68, 560, 0xFFFF, 2)
    elseif e == 98 then
        rttr:GetWorld():AddStaticObject(234, 68, 561, 0xFFFF, 2)
    elseif e == 99 then
        activeEvents[98] = true
        showMissionText(14)
        rttr:SetCampaignChapterCompleted('FANpaign', 1)
        rttr:EnableCampaignChapter('FANpaign', 2)
    end

    if not isLoading then
        local n = #eventHistory
        eventHistory[n + 1] = e
        eventHistory[n + 2] = trigger
    end

    activeEvents[trigger] = false
end

function onGameFrame(gf)
    if activeEvents[50] then
        triggerEndEvent(50, 50)
    end
    if activeEvents[98] then
        triggerEndEvent(98, 98)
    end
end

function onExplored(p, x, y, o)
    if false then -- dummy if for onContact cases
    elseif activeEvents[2] and ((p == 0 and o == 5) or (p == 5 and o == 0)) then
        showMissionText(4)
        triggerEndEvent(2, 2)
    elseif activeEvents[3] and ((p == 0 and o == 2) or (p == 2 and o == 0)) then
        showMissionText(6)
        triggerEndEvent(3, 3)
    end

    if p ~= 0 then return
    elseif activeEvents[4] and x == 175 and y == 150 then
        showMissionText(8)
        triggerEndEvent(4, 4)
    elseif activeEvents[5] and x == 121 and y == 19 then
        showMissionText(10)
        triggerEndEvent(5, 5)
    elseif activeEvents[6] and x == 244 and y == 105 then
        showMissionText(12)
        triggerEndEvent(6, 6)
    end
end

function onOccupied(p, x, y)
    if p ~= 0 then return
    elseif activeEvents[30] and x == 234 and y == 68 then
        triggerEndEvent(30, 30)
        triggerEndEvent(99, 30)
    end
end

